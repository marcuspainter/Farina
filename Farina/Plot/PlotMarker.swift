public enum PlotMarker: String, CaseIterable {
    case none = "?"
    case circle = "circle"
    case plusSign = "plus"
    case asterisk = "asterisk"
    case point = "circle.fill"
    case cross = "xmark"
    case horizontalLine = "minus"
    case verticalLine = "line.diagonal"
    case square = "square"
    case diamond = "diamond"
    case upwardTriangle = "arrowtriangle.up"
    case downwardTriangle = "arrowtriangle.down"
    case rightTriangle = "arrowtriangle.right"
    case leftTriangle = "arrowtriangle.left"
    case pentagram = "pentagon"
    case hexagram = "hexagon"
}
